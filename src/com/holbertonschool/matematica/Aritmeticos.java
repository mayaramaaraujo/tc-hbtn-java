package com.holbertonschool.matematica;

public class Aritmeticos {
    static int somar(int a, int b) {
        return a + b;
    }

    static int subtrair(int a, int b) {
        return a - b;
    }

    static int multiplicar(int a, int b) {
        return a * b;
    }

    static int dividir(int a, int b) {
        return a / b;
    }
}
